/**
 * OOPSBannerApp UC3 – Render OOPS as Banner using String.join()
        - using String.join() instead of +
 * @author Krish Aravind
 * @version 3.0
 */

public class OOPSBannerApp {

    public static void main(String[] args) {
        System.out.println(String.join(" ","  *****    " ,"  *****  ","  ******   ","   *****  "));
        System.out.println(String.join(" "," **   **   " ," **   ** ","  **   **  "," **    ** "));
        System.out.println(String.join(" ","**     **  " ,"**     **","  **   **  "," **       "));
        System.out.println(String.join(" ","**     **  " ,"**     **","  ******   ","  ******  "));
        System.out.println(String.join(" ","**     **  " ,"**     **","  **       ","       ** "));
        System.out.println(String.join(" "," **   **   " ," **   ** ","  **       "," **     **"));
        System.out.println(String.join(" ","  *****    " ,"  *****  ","  **       ","  ******  "));
        

    }
}